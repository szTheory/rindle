defmodule Rindle.Upload.Broker do
  @moduledoc """
  Manages direct-to-storage upload sessions.
  """

  alias Rindle.Config
  alias Rindle.Domain.{AssetFSM, MediaAsset, MediaUploadSession, UploadSessionFSM}
  alias Rindle.Security.StorageKey
  alias Rindle.Storage.Capabilities
  alias Rindle.Workers.PromoteAsset

  @default_multipart_part_size 8 * 1024 * 1024

  @doc """
  Initiates a new direct upload session.

  Creates a staged `MediaAsset` and an `initialized` `MediaUploadSession` in
  a single DB transaction, then emits `[:rindle, :upload, :start]`
  telemetry AFTER commit.

  ## Examples

      # Requires `config :rindle, :repo, MyApp.Repo` and a profile module.
      iex> {:ok, session} = Rindle.Upload.Broker.initiate_session(MyApp.MediaProfile, filename: "photo.png")
      iex> session.state
      "initialized"

  """
  def initiate_session(profile_module, opts \\ []) do
    repo = Config.repo()
    profile_name = profile_module_to_name(profile_module)
    filename = Keyword.get(opts, :filename, "unknown")
    extension = Path.extname(filename)

    # We generate a unique storage key upfront for the direct upload
    # Since we don't have the asset ID yet (will be generated by DB),
    # we use a temporary placeholder or generate the ID ourselves.
    asset_id = Ecto.UUID.generate()
    storage_key = StorageKey.generate(profile_name, asset_id, extension)

    expires_in_seconds = Keyword.get(opts, :expires_in, 3600)
    expires_at = DateTime.add(DateTime.utc_now(), expires_in_seconds, :second)

    case create_upload_session(
           repo,
           asset_id,
           profile_name,
           storage_key,
           filename,
           expires_at,
           %{state: "initialized"}
         ) do
      {:ok, session} ->
        emit_upload_start(profile_name, profile_module.storage_adapter(), session.id)

        {:ok, session}

      {:error, reason} ->
        {:error, reason}
    end
  end

  @doc """
  Initiates a new multipart upload session through the broker-owned lifecycle.
  """
  def initiate_multipart_session(profile_module, opts \\ []) do
    repo = Config.repo()
    profile_name = profile_module_to_name(profile_module)
    filename = Keyword.get(opts, :filename, "unknown")
    extension = Path.extname(filename)
    asset_id = Ecto.UUID.generate()
    storage_key = StorageKey.generate(profile_name, asset_id, extension)
    expires_in_seconds = Keyword.get(opts, :expires_in, 3600)
    expires_at = DateTime.add(DateTime.utc_now(), expires_in_seconds, :second)
    part_size = Keyword.get(opts, :part_size, @default_multipart_part_size)
    adapter = profile_module.storage_adapter()

    with :ok <- Capabilities.require_upload(adapter, :multipart_upload),
         {:ok, multipart} <- adapter.initiate_multipart_upload(storage_key, part_size, opts),
         {:ok, session} <-
           persist_multipart_session(
             repo,
             adapter,
             %{
               asset_id: asset_id,
               profile_name: profile_name,
               storage_key: storage_key,
               filename: filename,
               expires_at: expires_at
             },
             multipart,
             opts
           ) do
      emit_upload_start(profile_name, adapter, session.id)

      {:ok,
       %{
         session: session,
         multipart: %{
           upload_id: multipart.upload_id,
           upload_key: storage_key,
           part_size: Map.get(multipart, :part_size, part_size),
           part_headers: Map.get(multipart, :part_headers, %{})
         }
       }}
    else
      {:error, reason} -> {:error, reason}
    end
  end

  @doc """
  Generates a presigned URL for an initialized session.

  Transitions the session to `signed` and returns
  `{:ok, %{session: %MediaUploadSession{}, presigned: %{url: String.t()}}}`.

  ## Examples

      # Requires `config :rindle, :repo, MyApp.Repo` and a configured S3-compatible storage adapter.
      iex> {:ok, %{session: session, presigned: %{url: url}}} =
      ...>   Rindle.Upload.Broker.sign_url(session_id)
      iex> session.state
      "signed"
      iex> is_binary(url)
      true

  """
  def sign_url(session_id, opts \\ []) do
    repo = Config.repo()

    with %MediaUploadSession{} = session <- repo.get(MediaUploadSession, session_id),
         :ok <- UploadSessionFSM.transition(session.state, "signed", %{session_id: session.id}),
         asset <- repo.preload(session, :asset).asset,
         {:ok, profile_module} <- profile_name_to_module(asset.profile),
         adapter <- profile_module.storage_adapter(),
         expires_in <- Keyword.get(opts, :expires_in, 3600),
         {:ok, presigned} <- adapter.presigned_put(session.upload_key, expires_in, opts) do
      repo.transaction(fn ->
        {:ok, updated_session} =
          session
          |> MediaUploadSession.changeset(%{state: "signed"})
          |> repo.update()

        %{session: updated_session, presigned: presigned}
      end)
    else
      nil -> {:error, :not_found}
      {:error, reason} -> {:error, reason}
    end
  end

  @doc """
  Signs a multipart upload part without falling back to the presigned PUT path.
  """
  def sign_multipart_part(session_id, part_number, opts \\ []) do
    repo = Config.repo()

    with %MediaUploadSession{} = session <- repo.get(MediaUploadSession, session_id),
         :ok <- ensure_multipart_session(session),
         asset <- repo.preload(session, :asset).asset,
         {:ok, profile_module} <- profile_name_to_module(asset.profile),
         adapter <- profile_module.storage_adapter(),
         :ok <- Capabilities.require_upload(adapter, :multipart_upload),
         expires_in <- Keyword.get(opts, :expires_in, 3600),
         {:ok, presigned} <-
           adapter.presigned_upload_part(
             session.upload_key,
             session.multipart_upload_id,
             part_number,
             expires_in,
             opts
           ),
         {:ok, updated_session} <- ensure_session_marked_signed(repo, session) do
      {:ok, %{session: updated_session, presigned: presigned}}
    else
      nil -> {:error, :not_found}
      {:error, reason} -> {:error, reason}
    end
  end

  @doc """
  Completes a multipart upload, then converges into the trusted verification lane.
  """
  def complete_multipart_upload(session_id, parts, opts \\ []) do
    repo = Config.repo()

    with %MediaUploadSession{} = session <- repo.get(MediaUploadSession, session_id),
         :ok <- ensure_multipart_session(session),
         {:ok, normalized_parts} <- normalize_multipart_parts(parts),
         asset <- repo.preload(session, :asset).asset,
         {:ok, profile_module} <- profile_name_to_module(asset.profile),
         adapter <- profile_module.storage_adapter(),
         :ok <- Capabilities.require_upload(adapter, :multipart_upload),
         {:ok, persisted_session} <-
           update_session(repo, session, %{
             multipart_parts: %{"parts" => encode_multipart_parts(normalized_parts)}
           }),
         {:ok, _result} <-
           adapter.complete_multipart_upload(
             persisted_session.upload_key,
             persisted_session.multipart_upload_id,
             normalized_parts,
             opts
           ) do
      verify_completion(persisted_session.id, opts)
    else
      nil -> {:error, :not_found}
      {:error, reason} -> {:error, reason}
    end
  end

  @doc """
  Verifies that a direct upload was completed in storage.

  Transitions the session to `completed` and the asset to `validating`,
  then enqueues `PromoteAsset`. Emits
  `[:rindle, :upload, :stop]` telemetry AFTER the `Ecto.Multi` commits.

  ## Examples

      # Requires `config :rindle, :repo, MyApp.Repo`, the upload object to exist in storage, and the default `Oban` instance running.
      iex> {:ok, %{session: session, asset: asset}} =
      ...>   Rindle.Upload.Broker.verify_completion(session_id)
      iex> session.state
      "completed"
      iex> asset.state
      "validating"

  """
  def verify_completion(session_id, opts \\ []) do
    repo = Config.repo()

    with %MediaUploadSession{} = session <- repo.get(MediaUploadSession, session_id),
         asset <- repo.preload(session, :asset).asset,
         {:ok, profile_module} <- profile_name_to_module(asset.profile),
         adapter <- profile_module.storage_adapter(),
         # Check storage for object existence (Pitfall 5)
         {:ok, metadata} <- adapter.head(session.upload_key, opts),
         :ok <-
           UploadSessionFSM.transition(session.state, "verifying", %{session_id: session.id}),
         :ok <- AssetFSM.transition(asset.state, "validating", %{asset_id: asset.id}) do
      execute_verify_completion(repo, session, asset, profile_module, metadata)
    else
      nil -> {:error, :not_found}
      {:error, :not_found} -> {:error, :storage_object_missing}
      {:error, reason} -> {:error, reason}
    end
  end

  defp execute_verify_completion(repo, session, asset, profile_module, metadata) do
    Ecto.Multi.new()
    |> Ecto.Multi.update(
      :verifying_session,
      MediaUploadSession.changeset(session, %{state: "verifying"})
    )
    |> Ecto.Multi.run(:verify_fsm_complete, fn _repo, %{verifying_session: vs} ->
      do_fsm_transition(vs)
    end)
    |> Ecto.Multi.update(
      :session,
      fn %{verifying_session: vs} ->
        MediaUploadSession.changeset(vs, %{
          state: "completed",
          verified_at: DateTime.utc_now()
        })
      end
    )
    |> Ecto.Multi.update(
      :asset,
      MediaAsset.changeset(asset, %{
        state: "validating",
        byte_size: Map.get(metadata, :size),
        content_type: Map.get(metadata, :content_type)
      })
    )
    |> Oban.insert(:promote_job, PromoteAsset.new(%{asset_id: asset.id}))
    |> repo.transaction()
    |> case do
      {:ok, %{session: updated_session, asset: updated_asset}} ->
        :telemetry.execute(
          [:rindle, :upload, :stop],
          %{system_time: System.system_time()},
          %{
            profile: asset.profile,
            adapter: profile_module.storage_adapter(),
            session_id: updated_session.id,
            asset_id: updated_asset.id
          }
        )

        {:ok, %{session: updated_session, asset: updated_asset}}

      {:error, _name, reason, _changes} ->
        {:error, reason}
    end
  end

  defp do_fsm_transition(vs) do
    case UploadSessionFSM.transition(vs.state, "completed", %{session_id: vs.id}) do
      :ok -> {:ok, :transitioned}
      err -> err
    end
  end

  defp create_upload_session(
         repo,
         asset_id,
         profile_name,
         storage_key,
         filename,
         expires_at,
         session_attrs
       ) do
    case repo.transaction(fn ->
           {:ok, asset} =
             %MediaAsset{id: asset_id}
             |> MediaAsset.changeset(%{
               state: "staged",
               profile: profile_name,
               storage_key: storage_key,
               filename: filename
             })
             |> repo.insert()

           {:ok, session} =
             %MediaUploadSession{}
             |> MediaUploadSession.changeset(
               Map.merge(
                 %{
                   asset_id: asset.id,
                   state: "initialized",
                   upload_key: storage_key,
                   expires_at: expires_at
                 },
                 session_attrs
               )
             )
             |> repo.insert()

           session
         end) do
      {:ok, session} -> {:ok, session}
      {:error, reason} -> {:error, reason}
    end
  end

  defp persist_multipart_session(repo, adapter, session_seed, multipart, opts) do
    case create_upload_session(
           repo,
           session_seed.asset_id,
           session_seed.profile_name,
           session_seed.storage_key,
           session_seed.filename,
           session_seed.expires_at,
           %{
             state: "initialized",
             upload_strategy: "multipart",
             multipart_upload_id: multipart.upload_id,
             multipart_parts: %{}
           }
         ) do
      {:ok, session} ->
        {:ok, session}

      {:error, reason} ->
        compensate_failed_multipart_persist(
          adapter,
          session_seed.storage_key,
          multipart.upload_id,
          opts
        )

        {:error, reason}
    end
  end

  defp compensate_failed_multipart_persist(adapter, storage_key, upload_id, opts) do
    case adapter.abort_multipart_upload(storage_key, upload_id, opts) do
      {:ok, _} ->
        :ok

      {:error, :not_found} ->
        :ok

      {:error, reason} ->
        require Logger

        Logger.warning("rindle.upload.broker.multipart_persist_compensation_failed",
          upload_key: storage_key,
          multipart_upload_id: upload_id,
          reason: inspect(reason)
        )

        :ok
    end
  end

  defp update_session(repo, session, attrs) do
    repo.transaction(fn ->
      {:ok, updated_session} =
        session
        |> MediaUploadSession.changeset(attrs)
        |> repo.update()

      updated_session
    end)
  end

  defp ensure_multipart_session(%MediaUploadSession{
         upload_strategy: "multipart",
         multipart_upload_id: upload_id
       })
       when is_binary(upload_id) and upload_id != "",
       do: :ok

  defp ensure_multipart_session(%MediaUploadSession{upload_strategy: "multipart"}),
    do: {:error, :multipart_upload_not_initialized}

  defp ensure_multipart_session(_session), do: {:error, {:upload_unsupported, :multipart_upload}}

  defp ensure_session_marked_signed(_repo, %MediaUploadSession{state: "signed"} = session),
    do: {:ok, session}

  defp ensure_session_marked_signed(repo, %MediaUploadSession{} = session) do
    with :ok <- UploadSessionFSM.transition(session.state, "signed", %{session_id: session.id}),
         {:ok, updated_session} <- update_session(repo, session, %{state: "signed"}) do
      {:ok, updated_session}
    end
  end

  defp normalize_multipart_parts(parts) when is_list(parts) do
    parts
    |> Enum.reduce_while({:ok, []}, fn part, {:ok, acc} ->
      case normalize_multipart_part(part) do
        {:ok, normalized_part} -> {:cont, {:ok, [normalized_part | acc]}}
        {:error, reason} -> {:halt, {:error, reason}}
      end
    end)
    |> case do
      {:ok, normalized_parts} ->
        {:ok, Enum.sort_by(normalized_parts, & &1.part_number)}

      {:error, reason} ->
        {:error, reason}
    end
  end

  defp normalize_multipart_parts(_parts), do: {:error, :invalid_multipart_parts}

  defp normalize_multipart_part(%{part_number: part_number, etag: etag})
       when is_integer(part_number) and part_number > 0 and is_binary(etag) do
    {:ok, %{part_number: part_number, etag: etag}}
  end

  defp normalize_multipart_part(%{"part_number" => part_number, "etag" => etag})
       when is_integer(part_number) and part_number > 0 and is_binary(etag) do
    {:ok, %{part_number: part_number, etag: etag}}
  end

  defp normalize_multipart_part(_part), do: {:error, :invalid_multipart_parts}

  defp encode_multipart_parts(parts) do
    Enum.map(parts, fn %{part_number: part_number, etag: etag} ->
      %{"part_number" => part_number, "etag" => etag}
    end)
  end

  defp emit_upload_start(profile_name, adapter, session_id) do
    :telemetry.execute(
      [:rindle, :upload, :start],
      %{system_time: System.system_time()},
      %{
        profile: profile_name,
        adapter: adapter,
        session_id: session_id
      }
    )
  end

  defp profile_module_to_name(module) do
    to_string(module)
  end

  defp profile_name_to_module(name) do
    {:ok, String.to_existing_atom(name)}
  rescue
    ArgumentError -> {:error, :unknown_profile}
  end
end
